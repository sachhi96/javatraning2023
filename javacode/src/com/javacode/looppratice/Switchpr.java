package com.javacode.looppratice;

public class Switchpr {
	public static void main(String[] args) {
		String s = "Khusi";

		switch (s) {
		case "nun": {
			System.out.println("horror movie");
			break;
		}
		case "Khusi": {
			System.out.println("romatic movie");
			break;
		}
		case "mirzapur": {
			System.out.println("gangstar movie");
			break;
		}
		default: {
			System.out.println("no movie");

		}
		}
	}
}
