package com.javacode.loop;

public class Switchconditionex {
	public static void main(String[] args) {
		String s = "do";

		switch (s) {
		case "sunday":
			System.out.println("Going outside 1");
			break;
		case "monday":
			System.out.println("Going outside 2");
			break;
		case "tuesday":
			System.out.println("Going outside 3");
			break;
		case "wednesday":
			System.out.println("Going outside 4");
			break;
		case "thrusday":
			System.out.println("Going outside 5");
			break;
		case "friday":
			System.out.println("Going outside 6");
			break;
		case "saturday":
			System.out.println("Going outside 7");
			break;
		default:
			System.out.println("weekdays");
		}
	}

}
